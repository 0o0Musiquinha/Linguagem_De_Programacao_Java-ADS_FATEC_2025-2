    public void preencherComboProduto(){
        ProdutoDAO produtoDAO = new ProdutoDAO();
        List<Produto> listaProdutos = produtoDAO.getProdutos();
        cmb_produtos.addItem("");
        for(Produto produto : listaProdutos){
            cmb_produtos.addItem(produto);
        }
    }
    
    public void preencherComboNota(){
        NotaDAO notaDAO = new NotaDAO();
        List<Nota> listaNotas = notaDAO.getNotas();
        cmb_notas.addItem("");
        for(Nota nota : listaNotas){
            cmb_notas.addItem(nota);
        }
    }

