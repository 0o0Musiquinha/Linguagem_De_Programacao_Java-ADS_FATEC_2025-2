    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        if(cmb_produtos.getSelectedItem().equals("") || cmb_notas.getSelectedItem().equals("") || txt_quantidade.getText().equals("")){
            JOptionPane.showMessageDialog(
                null,
                "É necessário que todos os campos estejam preenchidos!!",
                "Preencha todos os campos!!",
                JOptionPane.OK_OPTION
            );
        } else{
            ItemNota itemNota = new ItemNota();
            Nota nota = (Nota) cmb_notas.getSelectedItem();
            Produto produto = (Produto) cmb_produtos.getSelectedItem();
            
            itemNota.setQuantidade(Integer.parseInt(txt_quantidade.getText()));
            itemNota.setNta_Codigo(nota);
            itemNota.setPro_Codigo(produto);
            

            ItemNotaDAO itemnotaDAO = new ItemNotaDAO();
            itemnotaDAO.cadastrar(itemNota);

            limparFormulario();
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

