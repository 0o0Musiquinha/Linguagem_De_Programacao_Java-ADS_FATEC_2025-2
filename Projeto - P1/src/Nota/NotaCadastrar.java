    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        int tipo = -1;
        
        if(rdo_entrada.isSelected()){
            tipo = 1;
        } else if(rdo_saida.isSelected()){
            tipo = 2;
        }
        
        if(tipo == -1){
            JOptionPane.showMessageDialog(null, "É necessario definir o tipo da Nota a ser Inserida!!", "Tipo de Nota Não Selecionado!", JOptionPane.OK_OPTION);
        } else if(!cmb_cliente.getSelectedItem().equals("") && tipo == 1 || !cmb_fornecedor.getSelectedItem().equals("") && tipo == 2 || cmb_cliente.getSelectedItem().equals("") && cmb_fornecedor.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(
                    null,
                    "É necessário inserir o Usuario respectivo ao tipo de Nota desejado!"
                    + "\n  -Se for 'Entrada', insira um 'Fornecedor'"
                    + "\n  -Se for 'Saída', insira um 'Cliente'", 
                    "Tipo de Usuario Não é Compativel com a Nota!",
                    JOptionPane.OK_OPTION
            );
        } else{
            Nota nota = new Nota();
            if(cmb_cliente.getSelectedItem().equals("")){
                Fornecedor fornecedor = (Fornecedor) cmb_fornecedor.getSelectedItem();
                nota.setFor_Codigo(fornecedor);
            } else{
                Cliente cli = (Cliente) cmb_cliente.getSelectedItem();
                nota.setCli_Codigo(cli);
            }
            nota.setTip_Codigo(tipo);

            NotaDAO notaDAO = new NotaDAO();
            notaDAO.cadastrar(nota);

            limparFormulario();
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

