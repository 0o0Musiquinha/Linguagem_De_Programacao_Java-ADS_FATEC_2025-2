    public void preencherComboCliente(){
        ClienteDAO clienteDAO = new ClienteDAO();
        List<Cliente> listaClientes = clienteDAO.getClientes();
        cmb_cliente.addItem("");
        for(Cliente cliente : listaClientes){
            cmb_cliente.addItem(cliente);
        }
    }
    
    public void preencherComboFornecedor(){
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        List<Fornecedor> listaFornecedor = fornecedorDAO.getFornecedores();
        cmb_fornecedor.addItem("");
        for(Fornecedor fornecedor : listaFornecedor){
            cmb_fornecedor.addItem(fornecedor);
        }
    }

